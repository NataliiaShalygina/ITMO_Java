package Lab02;
public class Calculator {
    public int sum (int firstTeam, int secondTeam) {
        return firstTeam + secondTeam;
    }
    public double sum (double firstTeam, double secondTeam) {
        return firstTeam + secondTeam;
    }
    public long sum (long firstTeam, long secondTeam) {
        return firstTeam + secondTeam;
    }
    public int mul (int firstFactor, int secondFactor) {
        return firstFactor * secondFactor;
    }
    public double mul (double firstFactor, double secondFactor) {
        return firstFactor * secondFactor;
    }
    public long mul (long firstFactor, long secondFactor) {
        return firstFactor * secondFactor;
    }
    public int sub(int minuend, int subtrahend) {
        return minuend - subtrahend;
    }
    public double sub(double minuend, double subtrahend) {
        return minuend - subtrahend;
    }
    public long sub(long minuend, long subtrahend) {
        return minuend - subtrahend;
    }
    public double div(int dividend, int divider) {
        return dividend / divider;
    }
    public double div(long dividend, long divider) {
        return dividend / divider;
    }
    public double div(double dividend, double divider) {
        return dividend / divider;
    }
}
